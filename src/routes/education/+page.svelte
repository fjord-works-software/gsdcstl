<h1 class="text-2xl font-serif font-bold text-primary mb-2">Education</h1>
<p class="text-gray-500 text-sm mb-6">AKC German Shepherd Dog Breed Standard — Adopted May 1968, Revised February 1978</p>

<div class="prose max-w-none text-gray-700 space-y-4">

<h2 class="text-lg font-serif font-semibold text-primary-dark">General Appearance</h2>
<p>The first impression of a good German Shepherd Dog is that of a strong, agile, well-muscled animal, alert and full of life. It is well balanced, with harmonious development of the forequarter and hindquarter. The dog is longer than tall, deep-bodied, and presents an outline of smooth curves rather than angles.</p>

<h2 class="text-lg font-serif font-semibold text-primary-dark">Character</h2>
<p>The breed has a distinct personality marked by direct and fearless, but not hostile, expression, self-confidence and a certain aloofness that does not lend itself to immediate and indiscriminate friendships. The dog must be approachable, quietly standing its ground and showing confidence and willingness to meet overtures without itself making them.</p>

<h2 class="text-lg font-serif font-semibold text-primary-dark">Head</h2>
<p>The head is noble, cleanly chiseled, strong without coarseness, but above all not fine, and in proportion to the body. The head of the male is distinctly masculine, and that of the female distinctly feminine. The muzzle is long and strong with the lips firmly fitted. The skull is approximately parallel to the top of the muzzle. Viewed from the front, the forehead is only moderately arched.</p>

<h2 class="text-lg font-serif font-semibold text-primary-dark">Ears</h2>
<p>Ears are moderately pointed, in proportion to the skull, open toward the front, and carried erect when at attention. The ideal carriage of the ears is to have them in line with the bisecting line of the skull and to be pulled forward.</p>

<h2 class="text-lg font-serif font-semibold text-primary-dark">Eyes</h2>
<p>Of medium size, almond shaped, set a little obliquely and not protruding. The color is as dark as possible. Washed-out, light-colored eyes are undesirable as showing a lack of character, and are to be strongly faulted.</p>

<h2 class="text-lg font-serif font-semibold text-primary-dark">Teeth</h2>
<p>42 in number — 20 upper and 22 lower — are strongly developed and meet in a scissors bite in which part of the inner surface of the upper incisors meet and engage part of the outer surface of the lower incisors. An overshot jaw is a serious fault. An undershot jaw is a disqualifying fault.</p>

<h2 class="text-lg font-serif font-semibold text-primary-dark">Neck</h2>
<p>The neck is strong and muscular, clean-cut and relatively long, proportionate in size to the head and without loose folds of skin. When the dog is at attention or excited, the head is raised and the neck carried high; otherwise typical carriage of the head is forward rather than up and but little higher than the top of the shoulders, particularly in motion.</p>

<h2 class="text-lg font-serif font-semibold text-primary-dark">Proportion</h2>
<p>The German Shepherd Dog is longer than tall, with the most desirable proportion as 10 to 8½. The length is measured from the point of the prosternum or breast bone to the rear edge of the pelvis, the ischial tuberosity. The desirable long proportion is not derived from a long back, but from overall length with relation to height, which is achieved by length of forequarter and length of withers and hindquarter, viewed from the side.</p>

<h2 class="text-lg font-serif font-semibold text-primary-dark">Coat</h2>
<p>The ideal dog has a double coat of medium length. The outer coat should be as dense as possible, hair straight, harsh and lying close to the body. A slightly wavy outer coat, often of wiry texture, is permissible. The head, including the inner ear and foreface, and the legs and paws are covered with short hair, and the neck with longer and thicker hair. The rear of the forelegs and hind legs has somewhat longer hair extending to the pastern and hock respectively.</p>

<h2 class="text-lg font-serif font-semibold text-primary-dark">Color</h2>
<p>The German Shepherd Dog varies in color, and most colors are permissible. Strong rich colors are preferred. Pale, washed-out colors and blues or livers are serious faults. A white dog must be disqualified.</p>

<h2 class="text-lg font-serif font-semibold text-primary-dark">Gait</h2>
<p>A German Shepherd Dog is a trotting dog, and its structure has been developed to meet the requirements of its work. General Impression — The gait is outreaching, elastic, seemingly without effort, smooth and rhythmic, covering the maximum amount of ground with the minimum number of steps. At a walk it covers a great deal of ground, with long strides of both hind legs and forelegs.</p>

<h2 class="text-lg font-serif font-semibold text-primary-dark">Disqualifications</h2>
<ul class="list-disc pl-6 space-y-1">
	<li>Cropped or hanging ears</li>
	<li>Undershot jaw</li>
	<li>Docked tail</li>
	<li>White dogs</li>
	<li>Dogs with noses not predominantly black</li>
	<li>Any dog that attempts to bite the judge</li>
</ul>

