<section>
	<h2 class="text-base font-serif font-bold text-primary-dark">Article I — Name and Objects</h2>
	<p>The club is named <strong>The German Shepherd Dog Club of St. Louis, Inc.</strong> Its objects are to encourage quality breeding; protect breed interests; demonstrate the versatility of the German Shepherd Dog; conduct shows and trials; and publish literature advancing the breed.</p>
</section>

<section>
	<h2 class="text-base font-serif font-bold text-primary-dark">Article II — Membership</h2>
	<p>Five membership types: <strong>Regular, Household, Lifetime, Associate,</strong> and <strong>Junior</strong> (ages 9–17).</p>
	<ul class="list-disc pl-5 space-y-1 mt-2">
		<li>Regular, Household, and Lifetime members have voting privileges.</li>
		<li>Associate members have no voting rights.</li>
		<li>Junior members have waived dues and no voting rights.</li>
	</ul>
	<p class="mt-2"><strong>Annual dues:</strong> Associate $15 · Single $30 · Household $50 (payable by January 1).</p>
	<p class="mt-2">Applications require submission in person with two non-related sponsors and a 2/3 affirmative vote.</p>
</section>

<section>
	<h2 class="text-base font-serif font-bold text-primary-dark">Article III — Meetings and Voting</h2>
	<ul class="list-disc pl-5 space-y-1">
		<li>Club meetings: at least 6 times per year; quorum of 20% required.</li>
		<li>Board meetings: at least 6 times per year.</li>
		<li>No proxy or absentee voting permitted.</li>
	</ul>
</section>

<section>
	<h2 class="text-base font-serif font-bold text-primary-dark">Article IV — Governors and Officers</h2>
	<p>The Board consists of 7 members: 5 elected governors plus the President and 4 officers. Officers serve 2-year terms. No more than 2 unexcused absences are permitted.</p>
	<p class="mt-2"><strong>Officers:</strong> President · Vice-President · Recording Secretary · Treasurer · Corresponding Secretary</p>
</section>

<section>
	<h2 class="text-base font-serif font-bold text-primary-dark">Article V — Club Year, Annual Meeting, Elections</h2>
	<ul class="list-disc pl-5 space-y-1">
		<li>Fiscal year: January 1 – December 31.</li>
		<li>Elections held by secret ballot in January.</li>
		<li>Nominating committee selected in September.</li>
	</ul>
</section>

<section>
	<h2 class="text-base font-serif font-bold text-primary-dark">Articles VI–XI</h2>
	<p>Cover committees, discipline procedures, amendment processes, and parliamentary authority (Robert's Rules of Order).</p>
</section>

